#Adição -> +
adicao = 10 + 9
print('Adição:', adicao)

#Subtraçao -> -
subtracao = 10 - 9
print('Subtração:', subtracao)

#Multiplicação -> *
multiplicacao = 10 * 9
print('Multiplicação:', multiplicacao)

#Exponenciação -> ** 
exponenciacao = 10 ** 9
print('Exponenciação:', exponenciacao)

#Divisão 1 -> / -> retorna valor exato
divisao1 = 10 / 3
print('Divisão 1:', divisao1)

#Divisão 2 -> // -> retorna valor aredondado 
divisao2 = 10 // 3
print('Divisão 2:', divisao2)

#Resto da divisão -> %
resto = 10 % 9
print('Resto:',  resto)

#Precedeência - Drieta -> esquercda
#1º ( )
#2º **
#1º * / // %
#1º + -
conta = (1+1*2) ** (5+4/2)
print(conta)