#Função type() -> mostra o tipo daquele dado
print(type('dado'))

#str -> String -> texto
print(type("String")) 
print(type('String')) 

#int -> numero inteiro(sem casa descimal)
print(type(1))  
print(type(-1)) 
print(type(0))  

#float -> numero com ponto flutuante(com casa descimal)
print(type(1.2))
print(type(-1.2))

#bool -> boolean -> true or false
print(10 == 10) #true
print(10 == 1) #false
print(type(10 == 10)) 